/**
 * The implementation of the event rpc service.
 *
 * @author brett chaldecott
 */
public class EventRPCServiceHelper extends VerticalPanel {
    
    private EventRPCServiceHelper() {
    }
    
    /**
     * The singleton method responsible for retrieving the event service reference.
     * @return The reference to the event rpc service async.
     */
    public static EventRPCServiceAsync getService(){
        // Create the client proxy. Note that although you are creating the
        // service interface proper, you cast the result to the asynchronous
        // version of
        // the interface. The cast is always safe because the generated proxy
        // implements the asynchronous interface automatically.
        EventRPCServiceAsync service = (EventRPCServiceAsync) GWT.create(EventRPCService.class);
        // Specify the URL at which our service implementation is running.
        // Note that the target URL must reside on the same domain and port from
        // which the host page was served.
        //
        ServiceDefTarget endpoint = (ServiceDefTarget) service;
        String moduleRelativeURL = GWT.getModuleBaseURL() + "/desk/event/eventrpcservice";
        endpoint.setServiceEntryPoint(moduleRelativeURL);
        return service;
    }
}
